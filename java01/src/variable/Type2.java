package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		/*
		 이름 : 000
		 출생년도 : 0000년
		 키 : 000.00cm
		 */
		String name = "홍길동";
		int birth = 2023; 
		double height = 169.70;
		
		System.out.println("이름: "+name);
		System.out.println("출생년도: "+birth+"년");
		System.out.println("키: "+height+"cm");
	}
}
